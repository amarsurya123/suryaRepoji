package controlStatment;

public class SwitchStatment {

	public static void main(String[] args) 
	{
		int year=3; 
		switch(year)
		{
		case 1: System.out.println("you are in 1st year");
		break;
		case 2:System.out.println("you are in 2nd year");
		break;
		case 3:System.out.println("you are in 3nd year");
		break;
		case 4:System.out.println("you are in 4th year");
		break;
		default:System.out.println("Default year");
		}

	}

}
