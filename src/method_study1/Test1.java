package method_study1;

public class Test1
{      
	public void mul() //non-static method
	{
		int d=30;
		int f=40;
		int mul=d*f;
		System.out.println("multiplication is "+mul);
	}
	public static void mul1() //static method
	{
		int d=50;
		int f=40;
		int mul1=d*f;
		System.out.println("multiplication is "+mul1);
	}

	
}
